class Scanner:
    def main(self):
        input_string = ""
        tokens = input_string.split()
        for token in tokens:
            print(token)

    def nextInt(self):
        return 10

    def nextLine(self):

        return "favour"

    def next(self):

        return "apple"

    def nextDouble(self):
        return 5.00

    def nextBool(self):
        return True




